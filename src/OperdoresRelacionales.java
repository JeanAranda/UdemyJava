public class OperdoresRelacionales {
    public static void main(String[] args) {
        //Operadores Relacionales de comparaciÃ³n
        int a =3, b = 2;
        //Igualdad ==
        var resultado = a==b;
        System.out.println("resultado de a==b : " + resultado);
        //Distinto !=
        resultado = a!=b;
        System.out.println("resultado de a!=b = : " + resultado);
        // Operdor de mayor que
        resultado = a>b;
        System.out.println("resultado de mayor a>b= : " + resultado);
        //Operador Mayoe o igual que
        resultado = a>=b;
        System.out.println("resultado de mayor o igual de a>=b = : " + resultado);
        //Operador de Menor que
        resultado = a<b;
        System.out.println("resultado de menor que a<b= : " + resultado);
        //Operador de Menor o igual que
        resultado = a<=b;
        System.out.println("resultado de menor o igual que a<=b = : " + resultado);

    }
}
